    private void fabricanteBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fabricanteBoxActionPerformed
        int indice = fabricanteBox.getSelectedIndex();
        fabricante = (Fabricante) fabricantes.get(indice);
        ArrayList<Marca> temp = new ArrayList<>();

        for (Object obj : marcas) {
            Marca cada = (Marca) obj;
            if (cada.getFabricante().equals(fabricante)) {
                temp.add(cada);
            }
        }
        String[] listaMarcas = new String[temp.size()];

        for (int i = 0; i < temp.size(); i++) {
            listaMarcas[i] = temp.get(i).getNome();
        }
        marcaBox.setEnabled(true);
        marcaBox.setModel(new javax.swing.DefaultComboBoxModel<>(listaMarcas));

    }//GEN-LAST:event_fabricanteBoxActionPerformed

