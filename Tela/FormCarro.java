    private void salvarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarItemActionPerformed
        Marca m1 = null;
        for (Object obj : marcas) {
            Marca cada = (Marca) obj;
            if (cada.getNome().equals(marcaBox.getSelectedItem())) {
                m1 = cada;
            }
        }

        int indice = fabricanteBox.getSelectedIndex();
        fabricante = (Fabricante) fabricantes.get(indice);

        if (nomeCampo.getText().isEmpty()
                || !marcaBox.isEnabled()
                || marcaBox.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Em branco");
            nomeCampo.grabFocus();

        } else {
            int max = 0;
            for (Object obj : lista) {
                Carro cada = (Carro) obj;
                max = (cada.getId() > max) ? cada.getId() : max;
            }

            Carro item = new Carro();
            item.setId(max + 1);
            item.setNome(Generico.campoFormatado(nomeCampo));
            item.setFabricante(fabricante);
            item.setMarca(m1);
            item.setNotas(notasCampo.getText().replaceAll("\\s+", " ").trim());
            item.setPendencia(pendenciaCampo.getText().replaceAll("\\s+", " ").trim());
            item.setPlaca(placaCampo.getText());
            item.setChassi(Generico.campoFormatado(chassiCampo));
            item.setTipo((String) tipoBox.getSelectedItem());
            item.setStatus((String) statusBox.getSelectedItem());

            if (lista.contains(item)) {
                JOptionPane.showMessageDialog(null, "Já existe");
                nomeCampo.grabFocus();

            } else {
                lista.add(item);
                Object linha[] = {item.getId(), item.getNome(), item.getFabricante().getNome(),
                    item.getMarca().getNome(), item.getPlaca(), item.getTipo()};
                tab.addRow(linha);
                try {
                    Generico.salvaFicheiro(arquivo, lista);
                    JOptionPane.showMessageDialog(null, item.getNome() + " criado");
                    quantidade.setText("QUANTIDADE: " + Integer.toString(lista.size()));
                    jTable1.requestFocus();
                    jTable1.changeSelection(0, 0, false, false);
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_salvarItemActionPerformed

