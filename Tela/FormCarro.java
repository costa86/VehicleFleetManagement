    private void atualizarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarItemActionPerformed
        Marca m1 = null;
        for (Object obj : marcas) {
            Marca cada = (Marca) obj;
            if (cada.getNome().equals(marcaBox.getSelectedItem())) {
                m1 = cada;
            }
        }
        this.setTitle("CARRO");
        int liberado = 1;
        int linhaAtual = jTable1.getSelectedRow();
        int id = (int) jTable1.getValueAt(linhaAtual, 0);

        if (marcaBox.getSelectedItem() == null) {
            liberado = 0;
            JOptionPane.showMessageDialog(null, "Falta marca");
        }

        for (Object obj : lista) {
            Carro cada = (Carro) obj;
            if (cada.getId() == id) {
                carro = cada;
            } else if (cada.getNome().equals(Generico.campoFormatado(nomeCampo)) && cada.getId() != id) {
                liberado = 0;
                JOptionPane.showMessageDialog(null, "Ja há");
            }
        }
        if (liberado == 1) {
            for (Object obj : marcas) {
                Marca cada = (Marca) obj;
                if (cada.getNome().equals(marcaBox.getSelectedItem()));
                marca = cada;
            }
            carro.setNome(Generico.campoFormatado(nomeCampo));
            carro.setFabricante(fabricante);
            carro.setMarca(m1);
            carro.setNotas(notasCampo.getText().replaceAll("\\s+", " ").trim());
            carro.setPendencia(pendenciaCampo.getText().replaceAll("\\s+", " ").trim());
            carro.setPlaca(placaCampo.getText());
            carro.setChassi(Generico.campoFormatado(chassiCampo));
            carro.setTipo((String) tipoBox.getSelectedItem());
            carro.setStatus((String) statusBox.getSelectedItem());
            JOptionPane.showMessageDialog(null, carro.toString());
            try {
                Generico.salvaFicheiro(arquivo, lista);
                JOptionPane.showMessageDialog(null, carro.getNome() + " editado");
                jTable1.requestFocus();
                jTable1.changeSelection(0, 0, false, false);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
            jTable1.setValueAt(carro.getId(), linhaAtual, 0);
            jTable1.setValueAt(carro.getNome(), linhaAtual, 1);
            jTable1.setValueAt(carro.getFabricante().getNome(), linhaAtual, 2);
            jTable1.setValueAt(carro.getMarca().getNome(), linhaAtual, 3);
            jTable1.setValueAt(carro.getPlaca(), linhaAtual, 4);
            jTable1.setValueAt(carro.getTipo(), linhaAtual, 5);
        }

    }//GEN-LAST:event_atualizarItemActionPerformed

